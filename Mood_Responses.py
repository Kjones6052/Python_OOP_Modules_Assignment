def mood_response(mood):
    if mood == "excited":
        print("That's fantastic, I'm happy to hear that!")
    elif mood == "good":
        print("That's awesome, I hope it continues!")
    elif mood == "happy":
        print("Cheers to a happy day!")
    elif mood == "sad":
        print("That's unfortunate, but I hope it gets better for you!")
    else:
        print("Please choose a mood from the options and try again.")